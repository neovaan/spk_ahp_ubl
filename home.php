    <h2 style="margin-top:0; color:#2c3e50;">
        🎯 Penentuan Pegawai Terbaik Metode AHP
    </h2>

    <p style="font-size:14px; color:#555; line-height:1.6;">
        Sistem ini membantu pengambil keputusan dalam menyeleksi <b>Pegawai Terbaik</b> dengan metode
        <b>Analytical Hierarchy Process (AHP)</b> berdasarkan perbandingan setiap kandidat pada setiap kriteria.
    </p>

    <hr style="border:0; border-top:1px solid #ddd; margin:15px 0;">

    <h4 style="margin-bottom:10px; color:#34495e;">📌 Proses yang harus dilakukan:</h4>

    <ol style="padding-left:18px; line-height:1.8; color:#444;">
        <li>Masukkan <b>Kriteria</b> yang dibutuhkan.</li>

        <li>Masukkan <b>Sub Kriteria</b> dari setiap kriteria.</li>

        <li>Masukkan <b>Alternatif / Kandidat Pegawai</b>.</li>

        <li>
            Mulai seleksi menggunakan metode AHP:
            <ul style="margin-top:8px;">
                <li>Isi <b>Bobot Kriteria</b> untuk menentukan prioritas kriteria.</li>
                <li>Isi <b>Bobot Sub Kriteria</b> sesuai perbandingan.</li>
                <li>Isi <b>Bobot Alternatif</b> pada setiap kriteria.</li>
                <li>Lihat hasil di menu <b>Perhitungan</b> untuk ranking akhir.</li>
            </ul>
        </li>

        <li>
            Cetak laporan:
            <span style="color:#2c3e50;">
                Kriteria, Sub Kriteria, Alternatif, dan Hasil Ranking.
            </span>
        </li>
    </ol>

    <div style="margin-top:15px; padding:10px; background:#e8f4ff; border-left:5px solid #3498db; border-radius:5px;">
        💡 <b>Tips:</b> Pastikan semua data sudah lengkap sebelum melakukan perhitungan agar hasil AHP lebih akurat.
    </div>

</div>